package com.silsub3.model.vo;

// 미완
public class Circle {
	public static double PI = 3.14;
	private static int radius;
	
	public void Circle(int radius) {
		this.radius = radius;
	}
	
	public void setPI(double PI) {
		this.PI = PI;
	}
	public double getPi() {
		return PI;
	}
	
	public void setRadius(int radius) {
		this.radius = radius;
	}
	public int getRadius() {
		return radius;
	}
	
	
	
	
	
}
